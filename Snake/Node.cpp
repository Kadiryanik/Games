#include "Node.h"

/*------------------------------------------------------------------------------*/
Node::Node(){
  prev = NULL;
  next = NULL;
  posX = 0;
  posY = 0;
}

/*------------------------------------------------------------------------------*/
Node::~Node(){
}

/*------------------------------------------------------------------------------*/
void Node::setPos(int x, int y){
  posX = x;
  posY = y;
}

/*------------------------------------------------------------------------------*/
void Node::setLink(Node *p, Node *n){
  this->prev = p;
  this->next = n;
}
